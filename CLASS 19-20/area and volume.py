print("find the area and length of a cylinder")
radius=eval(input("Enter the radius of a cylinder : "))
length=eval(input("Enter the length of a cylinder : "))
pi = 3.142
area = (radius * radius * pi)
volume = (area*length)
print ("the area of a cylinder is : ", area)
print ("the volume of a cylinder is : ",volume)      
      
       
              